private void fileButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fileButtonActionPerformed
    //open file chooser
        //The default dir to use if no value is stored
        File home = new File(System.getProperty("user.home"));
        FileChooserBuilder bldr = new FileChooserBuilder("DownloadDirectory").setTitle("Select Folder").
                setDefaultWorkingDirectory(home).setApproveText("Okay").setDirectoriesOnly(true);

        File toAdd = bldr.showSaveDialog();
        //Result will be null if the user clicked cancel or closed the dialog w/o OK
        if (toAdd != null) {
            //NbPreferences.forModule(FileChooserBuilder.class).put("DownloadDirectory", toAdd.getName());
            file = toAdd;
            fileText.setText(file.getAbsolutePath());
        }
}//GEN-LAST:event_fileButtonActionPerformed

